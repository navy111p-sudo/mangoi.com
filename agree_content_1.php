(2) 이 약관에 동의하는 것은 정기적으로 회사를 방문하여 약관의 변경사항을 확인하는 것에 동의함을 의미합니다. 변경된 약관에 대한 정보를 알지 못해 발생하는 이용자의 피해는 회사에서 책임지지 않습니다.<br>
(3) 회원은 변경된 약관에 동의하지 않을 경우 회원 탈퇴를 요청할 수 있으며, 변경된 약관의 효력 발생일로부터 7일 이후에도 거부의사를 표시하지 아니하고 서비스를 계속 사용할 경우 약관의 변경 사항에 동의한 것으로 간주됩니다.

<b>제 4 조 약관 외 준칙</b>
(1) 이 약관은 회사가 제공하는 개별서비스에 관한 이용안내(이하 “서비스별 안내”라 합니다)와 함께 적용합니다.<br>
(2) 이 약관에 명시되지 아니한 사항에 대해서는 관계법령 및 서비스별 안내의 취지에 따라 적용할 수 있습니다.

<h4>제 2 장 이용계약 체결</h4>

<b>제 5 조 이용계약의 성립</b>
회원은 회사가 요청하는 소정의 양식에 정보를 입력하고 이용신청을 하며, 회사는 이용신청을 승낙함으로써 당사의 서비스를 제공 받으실 수 있습니다.

<b>제 6 조 이용신청</b>
(1) 회원으로 등록하여 서비스를 이용하기 위해서는 회사에서 요청하는 정보(이름, 주소, 연락처 등)를 가입신청시 입력하셔야 합니다.<br>
(2) 타인의 명의와 개인정보를 무단으로 도용하여 이용신청을 한 회원의 ID는 삭제되거나 관계법령에 따른 조치를 받을 수 있습니다.<br>
(3) 회원은 한번의 가입으로 모든 회사 사이트를 등록한 하나의 이용자 ID로 이용하게 됩니다.<br>
(4) 만 14세 미만 아동의 회원 가입의 경우 아래의 각 호의 기준에 따릅니다.<br>
① 법정대리인의 성명, 이메일 주소, 연락처, 가입신청자와의 관계를 입력해야 하며 법정대리인의 약관 동의 의사가 있어야 "회원"으로 등록됩니다.<br>
② 법정대리인의 이메일 주소로 제공된 회원가입 동의서에 법정대리인이 직접 서명(이메일 상의 동의함 버튼 클릭)을 하여야 하며, 이 회원가입 동의서가 "회사"에 도달된 시점에 약관동의 의사가 있는 것으로 간주합니다.<br>
③ 법정대리인의 이메일 주소가 없는 경우 "회사"에서는 적절한 조치를 통해서 법정대리인이 회원 가입에 동의하도록 합니다.

<b>제 7 조 이용신청의 승낙 등</b>
(1) 회사는 회원이 제6조의 각 호를 기재하고 본 약관에 동의할 때 본 서비스의 이용을 승낙합니다.<br>
(2) 회사는 다음 각 호의 경우에 승낙을 철회하거나 거절할 수 있습니다.<br>
① 제 6조의 기재사항 및 기타 기재사항의 등록 내용에 허위, 기재누락, 도용, 오기가 있는 경우<br>
② 가입신청자가 이전에 회원자격을 상실한 사실이 있는 경우, 다만 회원자격 상실 후 3년이 경과한자로서 회사의 회원 재가입 승낙을 얻은 경우에는 예외로 합니다.<br>
③ 회원으로 등록하는 것이 회사의 기술상 현저히 지장이 있다고 판단되는 경우

<b>제 8 조 이용자ID 부여 및 변경 등</b>
(1) 회사는 이용고객에 대하여 약관에 정하는 바에 따라 이용자 ID를 부여합니다.<br>
(2) 이용자ID는 원칙적으로 변경이 불가하며 부득이한 사유로 변경 하고자 할때는 해당 ID를 해지하고 재가입해야 합니다.<br>
(3) 회사 사이트의 이용자ID는 이용자 본인의 동의 하에 사용할 수 있습니다.<br>
(4) 서비스 이용자ID 및 비밀번호의 관리책임은 이용자에게 있습니다. 이를 소홀이 관리하여 발생하는 서비스 이용상의 손해 또는 제3자에 의한 부정이용 등에 대한 책임은 이용자에게 있으며 회사는 그에 대한 책임을 일체 지지 않습니다.<br>
(5) 기타 이용자 개인정보 관리 및 변경 등에 관한 사항은 서비스별 안내에 정하는 바에 의합니다.

