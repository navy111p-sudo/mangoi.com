나. 고객의 부득이한 사정으로 고객센터와 상담을 거쳐 환불이 결정될 경우 다음과 같은 원칙으로 환불처리 됩니다.<br>
① 1개월 수강자의 경우에는 수업진행 후 7일 이내 환불 요청시 에만 환불이 가능합니다. (단 수업진행 7일 이후에는 환불이 되지 않음으로 이 점 유의하시기 바랍니다.) 환불금액은 고객님이 결제하신 화상 영어 서비스 수강료에서 서비스가 진행된 날짜의 수업료 및 결제수수료(카드,실시간 계좌이체, 가상계좌이체 등), 송금수수료, 교재비, 택배비, 화상 장비비(웹캠, 헤드셋 등)를 제한 나머지 금액을 환불해드립니다. 3개월이상의 장기 수강자의 경우 수업진행 7일 이후 환불 요청시 기본적인 환불조건(1개월 수강자와 동일)에 환불수수료 10%를 제한 금액을 환불해 드립니다.
(환불수수료는 총 결제금액의 10%로 규정하며 일일 수업료는 수업일자/수강비용으로 계산됩니다.)<br>
② 교재, 헤드셋, 화상캠 이벤트경품 유형의 상품에 대한 교환,환불,반품은 아래의 각호에 따릅니다.<br>
- 포장된 제품이 미개봉 상태로 단순 제품만 확인한 상태라면 회원이 상품 수령 후 7일 이내에 반품을 신청하면 회사는 별도의 수수료 없이 반품을 해야 합니다. 단, 반송을 위한 운송료는 회원이 부담합니다.<br>
- 제품을 수령한지 7일이 지났거나, 제품의 포장 비닐을 벗긴 경우는 반품이 불가능합니다.<br>
- 고객의 귀책사유로 인해 제품의 하자가 생긴 경우 반품 시 규정에 따라 손금이 부과됩니다.<br>
- 배송된 제품의 아웃케이스 또는 파손 등의 하자 발생 시 택배 사고의 유무에 관계없이 제품을 회사로 보내면, 제품상태 확인 후 새로운 제품으로 교환해 드리며, 운송료는 회사에서 부담합니다.<br>
* 회사는 소비자보호원 시행령을 준수합니다

<h4>제 5 장 계약 당사자의 권리와 의무</h4>

<b>제 16 조 회사의 의무</b>
(1) 회사는 이용자가 희망한 서비스 제공 개시일에 특별한 사정이 없는 한 서비스를 이용할 수 있도록 하여야 합니다.<br>
(2) 회사는 계속적이고 안정적인 서비스의 제공을 위하여 설비에 장애가 생기거나 멸실된 때에는 부득이한 사유가 없는 한 지체없이 이를 수리 또는 복구합니다.<br>
(3) 회사는 개인정보 보호를 위해 보안시스템을 구축하며 개인정보 보호정책을 공시하고 준수합니다.<br>
(4) 회사는 이용고객으로부터 제기되는 의견이나 불만이 정당하다고 객관적으로 인정될 경우에는 적절한 절차를 거쳐 즉시 처리하여야 합니다. 다만, 즉시 처리가 곤란한 경우는 이용자에게 그 사유와 처리일정을 통보하여야 합니다.

<b>제 17 조 이용자의 의무</b>
(1) 이용자는 서비스를 이용할 때 다음 각 호의 행위를 해서는 안됩니다.<br>
① 이용신청 또는 변경시 허위사실을 기재하는 행위<br>
② 다른 회원의 ID 및 비밀번호를 도용하여 공유하거나 부정하게 사용하는 행위<br>
③ 서비스에서 얻은 정보를 회사의 사전 승낙없이 회원의 이용 이외의 목적으로 복제하거나 이를 출판 및 방송 등에 사용하거나 제3자에게 제공하는 행위<br>
④ 회사의 저작권, 제3자의 저작권 등 기타 권리를 침해하는 행위<br>
⑤ 공공질서 및 미풍양속에 위반되는 내용의 정보, 문장, 도형 등을 타인에게 유포하는 행위<br>
⑥ 범죄와 결부된다고 객관적으로 판단되는 행위<br>
⑦ 기타 관계법령에 위배되는 행위<br>
(2) 이용자는 이 약관에서 규정하는 사항과 서비스 이용안내 또는 주의사항을 준수해야 합니다.<br>
(3) 이용자는 내용별로 회사가 서비스 공지사항에 게시하거나 별도로 공지한 이용 제한 사항을 준수해야 합니다.<br>
(4) 이용자는 회사의 명시적인 동의가 없는 한 서비스의 이용권한, 기타 이용 계약상의 지위를 타인에게 양도, 증여할 수 없으며 이를 담보로 제공할 수 없습니다.

<h4>제 6 장 계약 해지 및 이용 제한</h4>

<b>제 18 조 계약 해지 및 이용 제한</b>
(1) 회원이 이용 계약을 해지하고자 하는 경우에는 회원 본인이 회원 탈퇴를 회사에 신청하여야 합니다.<br>
(2) 회사는 회원이 다음 각 호의 1에 해당하는 행위를 하였을 경우 사전통지 없이 이용계약을 해지하거나 또는 기간을 정하여 서비스 이용을 중지할 수 있습니다.<br>
① 타인의 개인정보, ID 및 비밀번호를 도용한 경우<br>
② 가입한 이름이 실명이 아닌 경우<br>
③ 같은 사용자가 다른 ID로 이중 등록을 한 경우<br>
④ 타인의 명예를 손상시키거나 불이익을 주는 행위를 한 경우<br>
⑤ 회사, 다른 회원 또는 제3자의 지적재산권을 침해하는 경우<br>
⑥ 공공질서 및 미풍양속에 저해되는 내용을 고의로 유포시킨 경우<br>
⑦ 회원이 국익 또는 사회적 공익을 저해할 목적으로 서비스 이용을 계획 또는 실행하는 경우<br>
⑧ 서비스 운영을 고의로 방해한 경우<br>
⑨ 정보통신 설비의 오작동이나 정보의 파괴를 유발시키는 컴퓨터 바이러스 프로그램 등을 유포하는 경우<br>
⑩ 정보통신윤리위원회 등 외부기관의 시정요구가 있거나 불법선거운동과 관련하여 선거관리위원회의 유권해석을 받은 경우<br>
⑪ 회사의 서비스 정보를 이용하여 얻은 정보를 회사의 사전 승낙없이 복제 또는 유통시키거나 상업적으로 이용하는 경우<br>
⑫ 회원이 자사의 홈페이지와 게시판에 음란물을 게재하거나 음란 사이트 링크하는 경우<br>
⑬ 본 약관을 포함하여 기타 회사가 정한 이용 조건에 위반한 경우<br>
⑭ 회사의 명시적인 동의 없이, 회사가 제공하는 컨텐츠(강의 또는 교재)를 녹화 또는 복제하는 경우

<h4>제 7 장 손해배상 및 기타사항</h4>

<b>제 19 조 저작권</b>
1) 서비스내의 모든 디지털 컨텐츠에 대한 저작권은 회사에 있습니다.<br>
2) 회원은 회사가 제공하는 서비스를 이용함으로써 얻은 정보를 회사의 사전 승낙없이 복제ㆍ송신ㆍ출판ㆍ배포ㆍ방송 기타 방법에 의하여 영리 목적으로 이용하거나 제3자에게 이용하여서는 안됩니다.<br>
3) 회원이 작성한 게시물에 대한 모든 권리 및 책임은 이를 게시한 회원에게 있습니다.<br>
4) 회사는 회원이 회사에 제공한 공개적인 게시물을 사용ㆍ복제ㆍ수정ㆍ출판ㆍ배포 할 수 있습니다.

<b>제 20 조 손해배상</b>
1) 회사는 서비스 요금이 무료인 동안의 서비스 이용과 관련하여 회원에게 발생한 어떠한 손해에 관하여도 책임을 지지 않습니다.<br>
2) 회원이 이 약관의 규정을 위반하여 회사 또는 제3자에 손해가 발생하게 되는 경우, 이 약관을 위반한 회원은 회사 및 제3자에 발생한 손해를 배상할 책임이 있습니다.

<b>제 21 조 면책조항</b>
(1) 회사는 천재지변 또는 이에 준하는 불가항력으로 인하여 서비스를 제공할 수 없는 경우에는 서비스 제공에 관한 책임이 면제됩니다.<br>
(2) 회사는 회원의 귀책사유로 인한 서비스 이용의 장애에 대하여 책임을 지지 않습니다.<br>
(3) 회사는 회원이 서비스를 이용하여 기대하는 수익을 상실한 것이나 서비스를 통하여 얻은 자료로 인한 손해에 관하여 책임을 지지 않습니다.<br>
(4) 회사는 회원이 서비스에 게재한 정보, 자료, 사실의 신뢰도, 정확성 등 내용에 관하여는 책임을 지지 않습니다.<br>
(5) 회사는 서비스 이용과 관련하여 가입자에게 발생한 손해 가운데 가입자의 고의, 과실에 의한 손해에 대하여 책임을 지지 않습니다.

<b>제 22 조 재판권 및 준거법</b>
(1) 서비스 이용 중 발생한 회원과 회사간의 분쟁에 대해 소송이 제기될 경우 회사의 본사 소재지를 관할하는 법원을 관할법원으로 합니다.<br>
(2) 회사와 이용자간에 제기된 소송에는 대한민국국법을 적용합니다.<br>

