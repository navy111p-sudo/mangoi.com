1) 회사는는 이용자의 정보수집시 구매계약 이행에 필요한 최소한의 정보를 수집합니다. 다음 사항을 필수사항으로 하며 그 외 사항은 선택사항으로 합니다.<br>
    1. 성명<br>
    2. 주소<br>
    3. 전화번호<br>
    4. 희망ID(회원의 경우)<br>
    5. 비밀번호(회원의 경우)<br>
2) 회사가 이용자의 개인식별이 가능한 개인정보를 수집하는 때에는 반드시 당해 이용자의 동의를 받습니다.<br>
3) 제공된 개인정보는 당해 이용자의 동의 없이 목적 외의 이용이나 제3자에게 제공할 수 없으며, 이에 대한 모든 책임은 “회사”가 집니다. 다만, 다음의 경우에는 예외로 합니다.<br>
    1. 배송업무상 배송업체에게 배송에 필요한 최소한의 이용자의 정보(성명, 주소, 전화번호)를 알려주는 경우<br>
    2. 통계작성, 학술연구 또는 시장조사를 위하여 필요한 경우로서 특정 개인을 식별할 수 없는 형태로 제공하는 경우<br>
4) 회사가 제2항과 제3항에 의해 이용자의 동의를 받아야 하는 경우에는, 개인정보관리 책임자의 신원(소속, 성명 및 전화번호 기타 연락처), 정보의 수집목적 및 이용목적, 제3자에 대한 정보제공 관련사항(제공받는 자, 제공목적 및 제공할 정보의 내용)등 정보통신망 이용촉진 등에 관한 법률 제16조 제3항이 규정한 사항을 미리 명시하거나 고지해야 하며 이용자는 언제든지 이 동의를 철회할 수 있습니다.<br>
5) 이용자는 언제든지 회사가 가지고 있는 자신의 개인정보에 대해 열람 및 오류정정을 요구할 수 있으며 회사가 이에 대해 지체없이 필요한 조치를 취할 의무를 집니다. 이용자가 오류의 정정을 요구한 경우에는 회사가 그 오류를 정정할 때까지 당해 개인정보를 이용하지 않습니다.<br>
6) 회사는 개인정보 보호를 위하여 관리자를 한정하여 그 수를 최소화하며 신용카드, 은행계좌 등을 포함한 이용자의 개인정보의 분실, 도난, 유출, 변조 등으로 인한 이용자의 손해에 대하여 모든 책임을 집니다.<br>
7) 회사 또는 그로부터 개인정보를 제공받은 제3자는 개인정보의 수집목적 또는 제공받은 목적을 달성한 때에는 당해 개인정보를 지체없이 파기합니다.

<h4>제 4장 서비스 제공 및 이용</h4>

<b>제 9 조 유료서비스의 내용</b>
(1) 회사가 제공하는 유료서비스는 다음과 같습니다.<br>
가. 회사에서 판매되는 화상교육 서비스<br>
나. 회사에서 판매되는 영어교육 관련 서비스<br>
(2) 회사 사이트는 서비스의 내용 및 가격을 변경할 수 있습니다. 이 경우 변경된 서비스의 내용 및 적용일자를 사이트에 즉시 공지합니다.

<b>제 10 조 서비스 이용 시간</b>
(1) 서비스는 회사의 업무상, 기술상 문제 등 기타 특별한 사정이 없는 한 매일 24시간 운영함을 원칙으로 합니다.<br>
(2) 일부 서비스에 대해 회사 또는 이용자가 이용 시작 시간 및 이용 가능 시간을 별도로 정할 수 있습니다. 이 경우 서비스 이용안내를 통해 공지합니다.

<b>제 11 조 서비스 이용요금 및 결제방법</b>
(1) 유료서비스의 이용요금 지급방법은 다음 각 호의 방법 중 가용한 방법으로 할 수 있습니다.<br>
가. 일반결제수단<br>
① 신용카드 결제<br>
② 실시간 계좌이체<br>
③ 가상계좌입금<br>
나. 서비스결제수단<br>
(2) 서비스 이용요금에 관하여 약관에 명기되지 아니한 적용기준, 서비스이용요금 등 구체적인 사항은 회사 사이트에 별도로 공지하는 내용에 따릅니다.<br>
(3) 이용자는 이용요금의 납입의무를 충실히 수행하여야 하며, 이용요금의 미납으로 인하여 발생되는 모든 문제는 이용자 및 이용요금 납입자에게 있습니다.<br>
(4) 각 결제수단을 운영하는 사업자가 별도로 있는 경우, 회원은 해당 결제수단을 이용하기 전에 해당 결제수단의 사업자가 제시하는 절차를 이행하여야 합니다. 회사의 서비스 이용요금을 결제할 때에는 해당 결제수단의 사업자가 제시하는 절차 이행 및 이용약관에 동의한 것으로 간주합니다.<br>
(5) 회사가 이용요금 납입자에게 청구한 금액을 납입 기일 내에 납입하지 않은 경우 서비스 이용을 중지 할 수 있습니다.

<b>제 12 조 회원의 게시물</b>
회사는 회원이 게재 또는 등록하는 내용물이 다음 각 호에 해당된다고 판단하는 경우에 회원의 동의 없이 그 내용을 삭제할 수 있습니다.<br>
(1) 다른 회원 또는 제 3자를 비방하거나 중상모략 함으로써 타인의 명예를 훼손시킨 경우<br>
(2) 공공질서 및 미풍양속에 위반되는 경우<br>
(3) 범죄적 행위에 결부된다고 인정되는 경우<br>
(4) 제3자의 저작권 등 기타 권리를 침해하는 경우<br>
(5) 기타 관계 법령이나 회사에서 정한 규정에 위배되는 경우<br>
(6) 유료회원에서 무료회원으로 변경될 경우 회원이 작성한 일부 게시물은 용량제한에 따라 삭제함.

<b>제 13 조 서비스 제공의 중지 및 제한</b>
(1) 다음 각 호에 해당하는 경우 서비스 제공을 중지할 수 있습니다.<br>
가. 서비스용 설비의 보수 또는 공사로 인한 부득이한 경우<br>
나. 전기통신사업법에 규정된 기간통신사업자가 전기통신 서비스를 중지했을 경우<br>
다. 천재지변에 의해 부득이한 경우<br>
(2) 국가비상사태, 서비스 설비의 장애 또는 서비스 이용의 폭주 등으로 서비스 이용에 지장이 있을 때에는 서비스 제공을 중지하거나 제한할 수 있습니다.

<b>제 14 조 이용 승인</b>
(1) 회사에서 규정한 결제방법을 사용하여 이용 요금을 납입한 사실이 확인되면 지체없이 이용 신청을 승인합니다.<br>
(2) 회사는 아래에 해당하는 경우 이용신청에 대한 승인을 제한할 수 있고, 그 사유가 해소될 까지 승인을 유보할 수 있습니다.<br>
가. 유료 서비스의 이용 요금을 납입하지 않은 경우<br>
나. 유료 서비스 신청 금액 총액과 입금 금액 총액이 일치하지 않은 경우<br>
다. 기타 회사가 필요하다고 인정되는 경우<br>
(3) 제2항에 의하여 이용신청의 승인을 유보하거나 승인하지 아니하는 경우, 회사는 이를 이용신청자에 알려야 합니다. 다만, 회사의 귀책사유 없이 이용신청자에게 통지할 수 없는 경우는 예외로 합니다.

